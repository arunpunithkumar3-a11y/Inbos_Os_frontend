
import React from "react";
import { useWorkspace } from "../../contexts/WorkspaceContext";

export default function UserProfile() {
  const {
    user,
    setTourActive,
    setTourStep,
    setSkillsOpen,
    setSettingsOpen,
    handleLogout,
  } = useWorkspace();

  if (!user) return null;

  const displayName = user.email ? user.email.split("@")[0] : "User";
  const email = user.email || "";
  const avatarText = email ? email.substring(0, 2).toUpperCase() : "US";

  const handleRestartTour = () => {
    setTourActive(true);
    setTourStep(0);
  };

  return (
    <div className="sidebar-footer">
      <div className="user-profile">
        <div className="profile-avatar" id="user-avatar">
          {avatarText}
        </div>
        <div className="profile-meta">
          <span className="profile-name" id="user-display-name">
            {displayName}
          </span>
          <span className="profile-email" id="user-email-address">
            {email}
          </span>
        </div>
      </div>
      <div style={{ display: "flex", gap: "0.25rem", flexShrink: 0 }}>
        <button
          id="btn-open-tour"
          className="btn-icon"
          title="Onboarding Tutorial Tour"
          onClick={handleRestartTour}
        >
          <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
            <path d="M4 19.5A2.5 2.5 0 0 1 6.5 17H20"></path>
            <path d="M6.5 2H20v20H6.5A2.5 2.5 0 0 1 4 19.5v-15A2.5 2.5 0 0 1 6.5 2z"></path>
          </svg>
        </button>
        <button
          id="btn-open-skills"
          className="btn-icon"
          title="AI Capabilities & Skills"
          onClick={() => setSkillsOpen(true)}
        >
          <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
            <path d="M14.7 6.3a1 1 0 0 0 0 1.4l1.6 1.6a1 1 0 0 0 1.4 0l3.77-3.77a6 6 0 0 1-7.94 7.94l-6.91 6.91a2.12 2.12 0 0 1-3-3l6.91-6.91a6 6 0 0 1 7.94-7.94l-3.76 3.76z"></path>
          </svg>
        </button>
        <button
          id="btn-open-settings"
          className="btn-icon"
          title="Settings"
          onClick={() => setSettingsOpen(true)}
        >
          <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
            <circle cx="12" cy="12" r="3"></circle>
            <path d="M19.4 15a1.65 1.65 0 0 0 .33 1.82l.06.06a2 2 0 1 1-2.83 2.83l-.06-.06a1.65 1.65 0 0 0-1.82-.33 1.65 1.65 0 0 0-1 1.51V21a2 2 0 0 1-4 0v-.09A1.65 1.65 0 0 0 9 19.4a1.65 1.65 0 0 0-1.82.33l-.06.06a2 2 0 1 1-2.83-2.83l.06-.06a1.65 1.65 0 0 0 .33-1.82 1.65 1.65 0 0 0-1.51-1H3a2 2 0 0 1 0-4h.09A1.65 1.65 0 0 0 4.6 9a1.65 1.65 0 0 0-.33-1.82l-.06-.06a2 2 0 1 1 2.83-2.83l.06.06a1.65 1.65 0 0 0 1.82.33H9a1.65 1.65 0 0 0 1-1.51V3a2 2 0 0 1 4 0v.09a1.65 1.65 0 0 0 1 1.51 1.65 1.65 0 0 0 1.82-.33l.06-.06a2 2 0 1 1 2.83 2.83l-.06.06a1.65 1.65 0 0 0-.33 1.82V9a1.65 1.65 0 0 0 1.51 1H21a2 2 0 0 1 0 4h-.09a1.65 1.65 0 0 0-1.51 1z"></path>
          </svg>
        </button>
        <button
          id="btn-logout"
          className="btn-icon"
          title="Sign Out"
          onClick={handleLogout}
        >
          <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
            <path d="M9 21H5a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h4"></path>
            <polyline points="16 17 21 12 16 7"></polyline>
            <line x1="21" y1="12" x2="9" y2="12"></line>
          </svg>
        </button>
      </div>
    </div>
  );
}
